frutas = ["banana", "maçã", "pêra", "goiaba"]
print(frutas)

# Remoção do último valor:
frutas.pop()
print(frutas)

# Remoção de um valor em posição específica:
frutas.pop(0)
print(frutas)

# Remoção de um item específico:
frutas.remove("maçã")
print(frutas)

# Apagar a lista toda:
frutas.clear()
print(frutas)
