# Criando uma lista vazia:
frutas = []
print(type(frutas))

# Criando uma lista com elementos:
frutas = ["banana", "maçã", "pêra", "goiaba"]

# Exibir a lista completa:
print(frutas)

# Exibir uma posição específica da lista:
print (frutas[0])

# Exibir o último elemento da lista:
print (frutas[-1])
print (frutas[1:3])

# Exibir cada item da lista:
for i in frutas:
    print(i)